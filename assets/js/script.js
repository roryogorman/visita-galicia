/* Show selected province information */
/* The code for this was sourced from https://www.w3schools.com/howto/howto_js_toggle_hide_show.asp */

function myToggleFunction() {
  var province = document.getElementById("coruna");
  if (province.style.display === "none") {
    province.style.display = "block";
  } else {
    province.style.display = "none";
  }
}

